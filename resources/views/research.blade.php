<p>
My current research objectives can be summarised with the following four topics:
<ul>
    <li>
        Application of information-mining technologies to multidisciplinary
        data, including text- and data-mining of biomedical related information,
        with a particular emphasis on the use of semantic similarity measures to
        that effect. Data sources include health records, metabolic pathway
        descriptions, scientific publications, digital representations of
        biomedical entities, etc.
    </li>
    <li>
        Application of machine-learning algorithms (in particular neural
        networks and deep-learning algorithms) to biomedical data. I have not
        yet started to research this topic, but I will in the very near future.
    </li>
    <li>
        Exploration of several ideas to improve semantic similarity measures in
        a multidisciplinary content. For the moment, this consists in a set of
        measures of relevance of concepts in an ontology.
    </li>
    <li>
        Assessment of data quality by measuring some characteristics of the
        associated metadata, which including measuring the specificity and
        coverage of the metadata.
    </li>
</ul>
