<h2>Professional situation</h2>

<p>
I am currently a researcher at LaSIGE and an Invited Assistant Professor at the
Department of Informatics in the Faculty of Science of the University of Lisbon.
</p>

<h2>Background</h2>

<p>
I have a PhD in Bioinformatics, an MSc in Biochemistry and a degree in
Biochemistry. My strongly bio-oriented background allows me to have a particular
point of view over digital data from biology and other life-related scientes,
especially over its actual use and meaning, which the computer scientist in me
loves to explore.
</p>

<h2>Research interests</h2>

<p>
My research focusses generally in the area of Information Systems, in the
exploration of the knowledge encoded in ontologies applied to information
retrieval. Specifically, the main focus of my research is in semantic similarity
measures applied to multidisciplinary biomedical data (e.g. electronic health
records) as a basis to perform data mining and machine learning.
</p>

<p>
Other scientific interests of mine include:
<ul>
    <li>
        semantic web, namely with regard to the standard languages used to store
        and transfer the knowledge (<i>e.g.</i> RDF, OWL, SPARQL);
    </li>
    <li>
        text mining (especially how semantic similarity can be used to improve it);
    </li>
    <li>
        and web development.
    </li>
</ul>
</p>
